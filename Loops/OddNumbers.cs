using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1.Loops
{
    public class OddNumbers
    {
        public static void OddNumbers1()
        {
            int i = 1;
            while (i <= 100)
            {
                Console.Write(i + " ");
                i += 2;
            }
        }
    }
}
