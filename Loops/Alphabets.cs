using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1.Loops
{
    public class Alphabets
    {
        public static void Alphabets1(){
            char ch = 'a';
            while (ch <= 'z')
            {
                Console.Write(ch + " ");
                ch++;
            }
        }
    }
}
